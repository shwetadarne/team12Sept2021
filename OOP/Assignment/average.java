import java.util.*;
class average
{
	public static void main(String args[])
	{
		Scanner sc =new Scanner(System.in);
		System.out.println(" enter 3 numbers for average");
		int a=sc.nextInt();
		int b=sc.nextInt();
		int c=sc.nextInt();
		int d = (a+b+c)/3;
		
		System.out.println(" average of 3 numbers: "+d);
	}
}

		